from whatthelog.prefixtree.state import State

#****************************************************************************************************
# Edge
#****************************************************************************************************

class Edge:

    __slots__ = ['end']

    """
    Class representing an edge in a graph of states.
    """
    def __init__(self, end: State):
        """
        Edge constructor.

        :param end: End state.
        """
        self.end = hash(end)

    def __str__(self):
        return " -> " + str(self.end)

    def __repr__(self):
        return self.__str__()
